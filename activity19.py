#Listing

fruits = ["Orange", "Apple", "Strawberry", "Mango"]

print(f" My favorite fruits is {fruits[4]}. ")

#to add another item to the list
fruits.append("Avocado")
print(fruits)

#to insert an item to a specific index in the list
fruits.insert(2, "Melon")
print(fruits)

#to delete an item on a specific index in the list
fruits.remove('Orange')
print(fruits)


